async def seed():
    async with Session() as db:
        for module_id, content in CONTENT.items():
            result = await db.execute(
                select(LearningModule).where(LearningModule.id == module_id)
            )
            module = result.scalar_one_or_none()
            if module:
                module.content = content.strip()
                print(f"  ✅ {module.title}")
            else:
                print(f"  ⚠️  Module {module_id} not found, skip")

        await db.commit()
        print(f"\n✅ Content seeded for {len(CONTENT)} modules.")

if __name__ == "__main__":
    asyncio.run(seed())
